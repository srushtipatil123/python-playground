def addition(number1,number2):
    result = number1 + number2
    return result
def subtraction(num1,num2):
    res = num1 - num2
    return res
def multipication(num1,num2):
    res = num1 * num2
    return res
def division(num1,num2):
    res = num1 / num2
    return res
div = division(10,2)
print(f"The division of two numbers is: {div}")
mul = multipication(10,5)
print(f"The multication of two numbers is: {mul}")
sub = subtraction(10,5)
print(f"The subtraction of two numbers is: {sub}")
sum = addition(10,20)
print(f"The sum of two numbers is: {sum}")
